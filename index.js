function add(a, b) {
  a += b;
  return('number');
}
function subtract(a, b) {
  a -= b;
  return('number');
}
function multiply(a, b) {
  a *= b;
  return ('number');
}
function divide(a, b) {
  a /= b;
  return ('number');
}

function makeInt(string) {
  parseInt('string', 10);
  return 'string';
}

function preserveDecimal(string) {
  parseFloat('string');
  return 'string';
}
